CREATE TABLE JOBS(
    WORK_ID INT NOT NULL AUTO_INCREMENT,
    JOB_NAME VARCHAR(50),
    FNAME VARCHAR(20),
    LNAME VARCHAR(20),
    STREET VARCHAR(50),
    CITY VARCHAR(25),
    STATE VARCHAR(20),
    ZIP_CODE VARCHAR(5),
    PHONE_NUM VARCHAR(12),
    MATERIALS VARCHAR(120),
    DATE VARCHAR(10),
    HOURS REAL,
    STARTTIME VARCHAR(20),
    ENDTIME VARCHAR(20),
    NOTES VARCHAR(120),
    PDFS VARCHAR(100),
    IMAGES VARCHAR(100),
    STARTAM BOOLEAN,
    ENDAM BOOLEAN,
    PRIMARY KEY(WORK_ID)
)		